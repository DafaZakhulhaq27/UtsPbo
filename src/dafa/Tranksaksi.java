    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd") ;
        String tanggal = dateformat.format(tv_tanggal.getDate()) ;
        if("".equals(tv_nama.getText()) || "".equals(tv_beli.getText()) || "".equals(tv_harga.getText()) || "".equals(tv_minum.getText()) || "".equals(tanggal) ){
            JOptionPane.showMessageDialog(this, "Harap lengkapi Data","Error", JOptionPane.WARNING_MESSAGE);

        }else{

            harga = Integer.parseInt(tv_harga.getText()) ;
            if ( harga > 200000)
            {
                diskon1 = (10 * harga) / 100 ;
                diskon = harga - diskon1 ;

            }else{

                diskon1 = 0 ;
                diskon = harga ;

            }

            String SQL = "Insert Into tb_beli(no,nama_kasir,makan,minum,diskon,harga,total_harga,tgl)"+"Values("+null+",'"+tv_nama.getText()+"','"+tv_beli.getText()+"','"+tv_minum.getText()+"','"+diskon1+"','"+tv_harga.getText()+"','"+diskon+"','"+tanggal+"')" ;
            int status = KoneksiDB.execute(SQL) ;
            if(status == 1){

                JOptionPane.showMessageDialog(this, "Berhasil Ditambahkan","sukses",JOptionPane.INFORMATION_MESSAGE);
                selectdata();
            }else{

                JOptionPane.showMessageDialog(this, "Gagal Ditambahkan","gagal",JOptionPane.WARNING_MESSAGE);

            }

        }

    }//GEN-LAST:event_saveActionPerformed

