    private void selectdata() {
    
        String kolom[] = {"No","NamaKasir","Makan","Minum","Diskon","Harga","TotalHarga","Tanggal"} ;
        DefaultTableModel dtm = new DefaultTableModel(null, kolom) ;
        String SQL = "Select * From tb_beli " ;
        ResultSet rs = KoneksiDB.executeQuery(SQL) ;
        try {
            
            while(rs.next()){
                String No = rs.getString(1) ;
                String NamaKasir = rs.getString(2) ;
                String Makan = rs.getString(3) ;
                String Minum = rs.getString(4) ;
                String Diskon = rs.getString(5) ;
                String Harga = rs.getString(6) ;
                String TotalHarga = rs.getString(7) ;
                String Tanggal = rs.getString(8) ;
                String data[] = {No,NamaKasir,Makan,Minum,Diskon,Harga,TotalHarga,Tanggal} ;
                dtm.addRow(data);
            }
            
        }catch(SQLException ex){
            
            Logger.getLogger(Tranksaksi.class.getName()).log(Level.SEVERE, null, ex) ;
        }
        tabel.setModel(dtm);
        
    }
    
    
